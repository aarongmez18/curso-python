# AND
resultado = True & True    # Devuelve True
resultado2 = False & True  # Devuelve False
resultado3 = True & False  # Devuelve False
resultado4 = False & False # Devuelve False

print("AND")
print(resultado)
print(resultado2)
print(resultado3)
print(resultado4)

# OR
resultado = True | True    # Devuelve True
resultado2 = False | True  # Devuelve True
resultado3 = True | False  # Devuelve True
resultado4 = False | False # Devuelve False

print("OR")
print(resultado)
print(resultado2)
print(resultado3)
print(resultado4)

# NOT
resultado = not True       # Devuelve False
resultado2 = not False     # Devuelve True

print("NOT")
print(resultado)
print(resultado2)
print(resultado3)
print(resultado4)
